package Modelo;

/**
 *
 * @author Usuario
 */
public class Semestre {
    int id;
    String nom;
    String estado;

    public Semestre() {
    }

    public Semestre(int id, String nom, String estado) {
        this.id = id;
        this.nom = nom;
        this.estado = estado;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }
    
    
}
